package addTwoNumbers;

public class TwoNumbers {

	int num1=35;
	int num2=10;
	int result=0;
	
	void addTwoNumbers() {
		
		result =num1+num2;
		System.out.println(result);
	}
	
	void subtractTwoNumbers() {
		result =num1-num2;
		System.out.println(result);
    }
	
	void multiplyTwoNumbers() {
		result =num1*num2;
		System.out.println(result);
	}
}